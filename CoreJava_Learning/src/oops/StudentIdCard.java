package oops;

public class StudentIdCard {
	private String idCardnum;
	private String idCardvalidityDate;
	private String studentAccountNum;
	private String bloodGroup;
	public String getIdCardnum() {
		return idCardnum;
	}
	public void setIdCardnum(String idCardnum) {
		this.idCardnum = idCardnum;
	}
	public String getIdCardvalidityDate() {
		return idCardvalidityDate;
	}
	public void setIdCardvalidityDate(String idCardvalidityDate) {
		this.idCardvalidityDate = idCardvalidityDate;
	}
	public String getStudentAccountNum() {
		return studentAccountNum;
	}
	public void setStudentAccountNum(String studentAccountNum) {
		this.studentAccountNum = studentAccountNum;
	}
	public String getBloodGroup() {
		return bloodGroup;
	}
	public void setBloodGroup(String bloodGroup) {
		this.bloodGroup = bloodGroup;
	}
	
}
