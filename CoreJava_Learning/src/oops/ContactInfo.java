package oops;

public class ContactInfo {
	private int mobile;
	private int landline;
	private int emergencyNo;
	private int residentialAddress;
	public int getMobile() {
		return mobile;
	}
	public void setMobile(int mobile) {
		this.mobile = mobile;
	}
	public int getLandline() {
		return landline;
	}
	public void setLandline(int landline) {
		this.landline = landline;
	}
	public int getEmergencyNo() {
		return emergencyNo;
	}
	public void setEmergencyNo(int emergencyNo) {
		this.emergencyNo = emergencyNo;
	}
	public int getResidentialAddress() {
		return residentialAddress;
	}
	public void setResidentialAddress(int residentialAddress) {
		this.residentialAddress = residentialAddress;
	}
	
}
